#math functions

import math

pi = 3.14

print(round(pi)) # arrotonda il numero a quello più vicino
print(math.ceil(pi)) # arrotonda il numero a quello intero più vicino (crescendo)
print(math.floor(pi)) # arrotonda il numero a quello intero più vicino (decrescendo)
print(abs(pi)) # dice quanto un numero è lontano dallo 0
print(pow(pi,3)) # eleva il numero per tot volte (potenza di un numero)
print(math.isqrt(64)) # radice quadrata
print(math.factorial(90))

x = 1
y = 2
z = 3

print(max(x,y,z)) # dice il numero più grande tra una serie di numeri
print(min(x,y,z))  # dice il numero più piccolo tra una serie di numeri
